    public void get_groups()
    {
        
        try
        {
        Class.forName("oracle.jdbc.driver.OracleDriver");
        Connection con=DriverManager.getConnection("jdbc:oracle:thin:@59.93.119.150:1521:XE","hr","hr");
        PreparedStatement pstmt=con.prepareStatement("select group_name from user_rtr where user_name = ?");
        pstmt.setString(1,usr);
        ResultSet rs=pstmt.executeQuery();
        int i=0;
        while(rs.next())
        {
            groups[i]=rs.getString(1);
            i++;
        }
        int width=100,height=20;
        int y=30;
        for(j=0;j<groups.length&&groups[j]!=null;j++)
        {
            jcb[j]=new JCheckBox(groups[j],false);
            jcb[j].setVisible(true);
            jcb[j].setBounds(30,y,width,height);
            jList1.add(jcb[j]);
            y+=30;
            
                 
        }
            
        jList1.setVisible(true);
        
                        
        }
        catch(Exception ex)
        {  ex.printStackTrace(); }
    }
