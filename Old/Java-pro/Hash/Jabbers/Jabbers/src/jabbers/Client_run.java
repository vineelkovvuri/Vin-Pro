    public void run()
    {
        try {
             while(true)
	     {
	
                if(!s.isInputShutdown())
    		   {
    		     BufferedReader in=new BufferedReader(new InputStreamReader(s.getInputStream()));
    	             s1=in.readLine();
    	             if(s1!=null)
    	             jta1.setText(s1+"\n");
    		     else
                     {s.close();}
    		   	
                   }
                else
                {s.close();}
    		   	
              }
        } catch (IOException ex) {
            ex.printStackTrace();
        }
    }
